package com.maxaddon.api.maxbuild.payload;

/**
 *
 * @author nodejs
 */
public class JvmResponse {
    private Long id;
    private String name;
    private Long serverId;
    private Long clusterId;
    private String serverName;
    private String serverHost;
    private String clusterName;
    private String clusterIdentification;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getServerId() {
        return serverId;
    }

    public void setServerId(Long serverId) {
        this.serverId = serverId;
    }

    public Long getClusterId() {
        return clusterId;
    }

    public void setClusterId(Long clusterId) {
        this.clusterId = clusterId;
    }

    public String getServerName() {
        return serverName;
    }

    public void setServerName(String serverName) {
        this.serverName = serverName;
    }

    public String getServerHost() {
        return serverHost;
    }

    public void setServerHost(String serverHost) {
        this.serverHost = serverHost;
    }

    public String getClusterName() {
        return clusterName;
    }

    public void setClusterName(String clusterName) {
        this.clusterName = clusterName;
    }

    public String getClusterIdentification() {
        return clusterIdentification;
    }

    public void setClusterIdentification(String clusterIdentification) {
        this.clusterIdentification = clusterIdentification;
    }
    
    
}
