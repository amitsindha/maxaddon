package com.maxaddon.api.maxpost.payload;

import com.maxaddon.api.maxpost.model.Post;

/**
 *
 * @author nodejs
 */
public class CommentRequest {
    private Long id;
    private String text;
    private Post post;
    private Long postId;
    
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public Post getPost() {
        return post;
    }

    public void setPost(Post post) {
        this.post = post;
    }

    public Long getPostId() {
        return postId;
    }

    public void setPostId(Long postId) {
        this.postId = postId;
    }
    
    
    
}
